print('Cifrado CÃ©sar')
texto=input("Ingresa el texto: ")
N=int(input("Posiciones a recorrer: "))
print('')
if (len(texto) < 1) or (N<=1):
  print('Los valores no son los pedidos')
else:
  cifrad=''
  abc='AaBbCcDdEeFfGgHhIiJjKkLlMmNnOoPpQqRrSsTtUuVvWwXxYyZz'
  for c in texto:
    if c in abc:
      cifrad += abc[(abc.index(c)+(N*2)) % (len(abc))]
    else:
      cifrad += c
  print(f'El texto cifrado es: {cifrad}')